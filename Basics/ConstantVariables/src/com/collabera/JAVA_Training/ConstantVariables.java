package com.collabera.JAVA_Training;

public class ConstantVariables {

    public static void main(String[] args) {

        // Constant:

        // A constant variable means your variable remains a constant value as a result.
        // A "Constant" is a variable whose value cannot change once it has been assigned.
        // JAVA doesn't have a built-in support for constants, but the variable modifiers "static"
        // and "final" can be used to effectively create one.

         final float pi = 3.14159f;

        System.out.println(pi);


    }
}
